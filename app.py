def draw_info_text(image, brect, handedness, hand_sign_text,
                    active_finger_name):
    cv.rectangle(image, (brect[0], brect[1]), (brect[2], brect[1] - 22),
                 (0, 0, 0), -1)

    info_text = handedness.classification[0].label[0:]
    if hand_sign_text != "":
        info_text = info_text + ':' + hand_sign_text
    cv.putText(image, info_text, (brect[0] + 5, brect[1] - 4),
               cv.FONT_HERSHEY_SIMPLEX, 0.6, (255, 255, 255), 1, cv.LINE_AA)

    if active_finger_name != "":
        cv.putText(image, "Finger:" + active_finger_name,
                   (brect[0] + 5, brect[1] + 18),
                   cv.FONT_HERSHEY_SIMPLEX, 0.5, (0, 255, 255), 1,
                   cv.LINE_AA)
    return image


def detect_active_finger(landmark_point):
    if len(landmark_point) <= 20:
        return "", None

    finger_tips = {
        'Thumb': 4,
        'Index': 8,
        'Middle': 12,
        'Ring': 16,
        'Little': 20,
        'All': 8,
    }

    wrist_x, wrist_y = landmark_point[0]
    middle_mcp_x, middle_mcp_y = landmark_point[9]
    palm_size = math.hypot(middle_mcp_x - wrist_x, middle_mcp_y - wrist_y)

    max_distance = -1.0
    active_name = ''
    active_tip = None

    for finger_name, tip_index in finger_tips.items():
        tip_x, tip_y = landmark_point[tip_index]
        distance = math.hypot(tip_x - wrist_x, tip_y - wrist_y)
        if distance > max_distance:
            max_distance = distance
            active_name = finger_name
            active_tip = [tip_x, tip_y]

    if max_distance < palm_size * 1.15:
        return '', None

    return active_name, active_tip


